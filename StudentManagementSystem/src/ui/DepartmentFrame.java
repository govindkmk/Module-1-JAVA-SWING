    private void departmentTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_departmentTableMouseClicked
        // TODO add your handling code here:
         departmentId = Integer.parseInt(departmentTable.getValueAt(departmentTable.getSelectedRow(), 0).toString());
        
        Department department;
        try {
            department = departmentDbManager.getDepartmentById(departmentId);
         
            idTextLabel.setText(String.valueOf(department.getId()));
            nameTextLabel.setText(department.getName());
            codeTextLabel.setText(department.getCode());
        } catch (SQLException ex) {
            Logger.getLogger(DepartmentFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_departmentTableMouseClicked

