    private void getAllDepartmentsIntoTable(){
        
        departmentDefaultTableModel = new DefaultTableModel(columns, 0);
        
        List<Department> department = departmentDbManager.getAllDepartments();
        
        for(Department d : department){
            Object row[] = {d.getId(), d.getName(), d.getCode()};
            departmentDefaultTableModel.addRow(row);
            departmentTable.setModel(departmentDefaultTableModel);
        }
        
    }
    
    private Department getDepartment(){
        int id = Integer.parseInt(idTextLabel.getText().toString());
        String name = nameTextLabel.getText().toString();
        String code = codeTextLabel.getText().toString();
        
        Department department = new Department();
        department.setId(id);
        department.setName(name);
        department.setCode(code);
        
        return department;
    }
    private void clearFields(){
        idTextLabel.setText("");
        nameTextLabel.setText("");
        codeTextLabel.setText("");
    }

    
