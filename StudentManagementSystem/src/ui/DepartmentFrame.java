    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
       
        try {
            boolean success = departmentDbManager.deleteDepartment(departmentId);
            if(success){
                JOptionPane.showMessageDialog(this,"Department Deleted Successfully...");
                getAllDepartmentsIntoTable();
                clearFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(DepartmentFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        Department department = this.getDepartment();
        department.setId(departmentId);
        try {
            // TODO add your handling code here:
            
            boolean success = departmentDbManager.updateDepartment(department);
            
            if(success){
                JOptionPane.showMessageDialog(this, "Department Updated Successfully");
                getAllDepartmentsIntoTable();
                clearFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(DepartmentFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_updateButtonActionPerformed

